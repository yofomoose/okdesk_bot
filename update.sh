    echo "‚úÖ –ö–æ–¥ —É–∂–µ –∞–∫—Ç—É–∞–ª—å–Ω—ã–π, –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –Ω–µ —Ç—Ä–µ–±—É—é—Ç—Å—è"
    exit 0
