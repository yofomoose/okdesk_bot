    cp data/okdesk_bot.db $BACKUP_NAME
    echo "üíæ –°–æ–∑–¥–∞–Ω –±—ç–∫–∞–ø –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö: $BACKUP_NAME"
