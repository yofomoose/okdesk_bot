echo ""
echo "‚úÖ –û–ë–ù–û–í–õ–ï–ù–ò–ï –ó–ê–í–ï–†–®–ï–ù–û!"
echo ""
echo "üìã –ü–æ–ª–µ–∑–Ω—ã–µ –∫–æ–º–∞–Ω–¥—ã –ø–æ—Å–ª–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è:"
echo "  –ü–æ–ª–Ω—ã–µ –ª–æ–≥–∏:     docker-compose logs -f"
echo "  –°—Ç–∞—Ç—É—Å:          docker-compose ps"
