        sleep 5

        # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å—Ç–∞—Ç—É—Å
        RUNNING_AFTER=$(docker-compose ps | grep "Up" | wc -l)
        if [ "$RUNNING_AFTER" -gt 0 ]; then
