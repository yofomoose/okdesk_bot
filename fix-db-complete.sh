# –®–∞–≥ 4: –ò—Å–ø—Ä–∞–≤–ª–µ–Ω–∏–µ –ø—Ä–∞–≤ –Ω–∞ —Ö–æ—Å—Ç–µ
fix_host_permissions() {
    log "–®–∞–≥ 4: –ò—Å–ø—Ä–∞–≤–ª–µ–Ω–∏–µ –ø—Ä–∞–≤ –Ω–∞ —Ö–æ—Å—Ç–µ"

    # –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏—è —Ñ–∞–π–ª–∞
    if [ -f "./data/okdesk_bot.db" ]; then
        log "–§–∞–π–ª –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö –Ω–∞–π–¥–µ–Ω"

        # –¢–µ–∫—É—â–∏–µ –ø—Ä–∞–≤–∞
        ls -la ./data/okdesk_bot.db

        # –ò—Å–ø—Ä–∞–≤–ª–µ–Ω–∏–µ –ø—Ä–∞–≤
        if chmod 666 ./data/okdesk_bot.db; then
            log "‚úÖ –ü—Ä–∞–≤–∞ –Ω–∞ —Ñ–∞–π–ª –∏—Å–ø—Ä–∞–≤–ª–µ–Ω—ã"
        else
            error "‚ùå –ù–µ —É–¥–∞–ª–æ—Å—å –∏—Å–ø—Ä–∞–≤–∏—Ç—å –ø—Ä–∞–≤–∞ –Ω–∞ —Ñ–∞–π–ª"
