start_containers() {
    log "–®–∞–≥ 2: –ó–∞–ø—É—Å–∫ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–æ–≤"

    RUNNING=$(docker-compose ps | grep "Up" | wc -l)
    if [ "$RUNNING" -eq 0 ]; then
        log "–ö–æ–Ω—Ç–µ–π–Ω–µ—Ä—ã –Ω–µ –∑–∞–ø—É—â–µ–Ω—ã, –∑–∞–ø—É—Å–∫–∞—é..."
        docker-compose up -d

